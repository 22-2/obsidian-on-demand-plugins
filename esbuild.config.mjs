// @ts-check

import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import { obsidianCopyPlugin } from "@22-2/esbuild-plugin-obsidian-copy";
import dotenv from "dotenv";
dotenv.config();

const pluginsPath = process.env.PLUGINS_PATH;

if (!pluginsPath) {
	throw new Error("PLUGINS_PATH is not defined");
}

const banner =
`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === "production");

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "esnext",
	logLevel: "info",
	minify: prod,
	plugins: [
		obsidianCopyPlugin({
			pluginsDir: pluginsPath,
            targetDirName: "obsidian-lazy-plugins",
            force: true,
		}),
	],
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
